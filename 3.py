# Create a string
str1 = "Hello, "

# Concatenate strings
str2 = "Python!"
concatenated_str = str1 + str2

# Print the concatenated string
print("Concatenated String:", concatenated_str)

# Access substring
# Using slicing to get a substring
substring1 = concatenated_str[7:]
print("Substring 1:", substring1)

# Another way to get a substring using slicing
substring2 = concatenated_str[0:5]
print("Substring 2:", substring2)

# Access a specific character
char_at_index = concatenated_str[12]
print("Character at index 12:", char_at_index)
